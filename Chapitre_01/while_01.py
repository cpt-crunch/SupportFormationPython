# Démonstration d'utilisation d'un boucle while 

x = 1 # Variable ou "x = 1"
while x < 10: # Création de la boucle ou si 'x est inférieur à 10'
    print("x a pour valeur", x)
    x = x * 2 # Si inférieur 'x = x fois 2'

# Nous obtiendrons donc les résultats égaux à 'x' en dessous de 10

# Le mot-clé 'while' signifie 'tant que' en anglais. Le corps de la boucle sera répété tant que la condition est vraie
# Dans l'exemple ci-dessus, 'x' sera multiplié par 2 tant que la valeur reste inférieur à 10