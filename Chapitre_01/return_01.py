# Démonstration fonction simple qui return une valeur

def difference(a,b): # Création d'une fonction qui calcule la différence entre deux nombres
    return a - b
    
x = difference(10, 12) # On éxecute 'difference()' en lui passant deux nombres et on stock le résultat dans une variable 'x'
print(x) # 'x' contient la valeur -2


